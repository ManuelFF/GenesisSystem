    private void filtro() 
    {
          int columnaABuscar = 0;
          
          if(cbfiltro.getSelectedItem().toString().equals("Nombre/Razón"))
          {
              columnaABuscar = 2;
          }
          
          if(cbfiltro.getSelectedItem().toString().equals("Número Orden"))
          {
              columnaABuscar = 1;
          }

          if(cbfiltro.getSelectedItem().toString().equals("Fecha"))
          {
              columnaABuscar = 4;
          }
          
          if (cbfiltro.getSelectedItem().toString().equals("ID")) 
          {
              columnaABuscar = 0;
          }
          
          trsFiltro.setRowFilter(RowFilter.regexFilter(txtfiltro.getText(), columnaABuscar));
     }
    
    private void orden(String tipo_orden)
    {
        if(tipo_orden.equals("Juridica"))
        {
            P_BO.CargarOrdenJ(modelo, JTOrden);
        }
        
        if(tipo_orden.equals("Natural"))
        {
            P_BO.CargarOrdenN(modelo, JTOrden);
        }
        
    }

