    private void JTOrdenMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JTOrdenMouseClicked

    try {

        int row = JTOrden.rowAtPoint(evt.getPoint());

        if(formulario.equals("ORDEN_SERV"))
        {
            
            P_BO.iniciar(""+JTOrden.getValueAt(row, 0),buffer);
            
            evn.write(JFRPrincipal.JMSesion.getText(),"Ha seleccionado la orden "+JTOrden.getValueAt(row, 1),"GL_JDBuscarOrdenes", "Tabla de Orden presionada");
        }

        this.dispose();

    } catch (Exception e)
    {
        lc.write("Error al seleccionar orden", "GL_JDBuscarOrdenes", e);
    }


    }//GEN-LAST:event_JTOrdenMouseClicked

