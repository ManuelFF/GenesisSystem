    private void txtfiltroKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtfiltroKeyTyped

        txtfiltro.addKeyListener(new KeyAdapter()
            {
                public void keyReleased(final KeyEvent e)
                {
                    String cadena = (txtfiltro.getText().toUpperCase());
                    txtfiltro.setText(cadena);
                    repaint();
                    filtro();
                }
            });
        
        
    }//GEN-LAST:event_txtfiltroKeyTyped

    private void JTbuscarVendedorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JTbuscarVendedorMouseClicked

        try {

            int row = JTbuscarVendedor.rowAtPoint(evt.getPoint());

            if(formulario.equals("ORDEN_SERV"))
            {
                JIFGenerarOrdenTrabajo.txtidVendedor.setText(""+JTbuscarVendedor.getValueAt(row, 0));
                JIFGenerarOrdenTrabajo.txtnombrevendedor.setText(""+JTbuscarVendedor.getValueAt(row, 1));
                
            }

            String select = ""+JTbuscarVendedor.getValueAt(row, 1);

            evn.write(JFRPrincipal.JMSesion.getText(),"Ha seleccionado al Vendedor "+select,"GL_JDBuscarVendedor", "Tabla de Vendedor presionado");

            this.dispose();

        } catch (Exception e)
        {
            lc.write("Error al seleccionar vendedor", "GL_JDBuscarVendedor", e);
        }

    }//GEN-LAST:event_JTbuscarVendedorMouseClicked

